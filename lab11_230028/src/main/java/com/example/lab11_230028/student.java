public class student {
    private String name;
    private int age;

    public student() {
        this.name = "Ali";
        this.age = 21;
    }
    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
